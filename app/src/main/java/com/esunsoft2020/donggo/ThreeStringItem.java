package com.esunsoft2020.donggo;

public class ThreeStringItem {
    String imgUrl;
    String title;
    String count;

    public ThreeStringItem() {
    }

    public ThreeStringItem(String imgUrl, String title, String count) {
        this.imgUrl = imgUrl;
        this.title = title;
        this.count = count;
    }
}
